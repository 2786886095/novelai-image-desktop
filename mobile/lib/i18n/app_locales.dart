  return _mobileUiText[code]?[key] ?? _mobileUiText['zh-CN']?[key] ?? key;
}

String mobileUiFormatFor(
  Object? value,
  String key,
  Map<String, Object?> placeholders,
) {
  var text = mobileUiTextFor(value, key);
  for (final entry in placeholders.entries) {
    text = text.replaceAll('{${entry.key}}', entry.value?.toString() ?? '');
  }
  return text;
}

