import 'package:flutter/widgets.dart';

class AppLocaleInfo {
  final String code;
  final String nativeName;
  final String englishName;
  final Locale locale;

  const AppLocaleInfo({
    required this.code,
    required this.nativeName,
    required this.englishName,
    required this.locale,
  });

  String get menuLabel => nativeName;
}

