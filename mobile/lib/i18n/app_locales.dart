    locale: Locale('zh', 'CN'),
  ),
  AppLocaleInfo(
    code: 'zh-TW',
    nativeName: '繁體中文',
    englishName: 'Traditional Chinese',
    locale: Locale('zh', 'TW'),
  ),
  AppLocaleInfo(
    code: 'en-US',
    nativeName: 'English',
    englishName: 'English',
    locale: Locale('en', 'US'),
  ),
  AppLocaleInfo(
    code: 'ja-JP',
    nativeName: '日本語',
    englishName: 'Japanese',
    locale: Locale('ja', 'JP'),
  ),
  AppLocaleInfo(
    code: 'ko-KR',
    nativeName: '한국어',
    englishName: 'Korean',
    locale: Locale('ko', 'KR'),
  ),
];

const _supportedLocaleCodes = {'zh-CN', 'zh-TW', 'en-US', 'ja-JP', 'ko-KR'};

String normalizeAppLocaleCode(Object? value) {
  final raw = value?.toString();
  return _supportedLocaleCodes.contains(raw) ? raw! : 'zh-CN';
}

AppLocaleInfo appLocaleInfoFor(Object? value) {
  final code = normalizeAppLocaleCode(value);
  return supportedAppLocales.firstWhere((locale) => locale.code == code);
}

