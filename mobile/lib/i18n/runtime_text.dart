final _runtimeAliases = <String, Map<String, String>>{
  'zh-TW': _overlay(_runtimeText['zh-CN']!, _runtimeText['zh-TW']!),
  'en-US': _overlay(_runtimeText['zh-CN']!, _runtimeText['en-US']!),
  'ja-JP': _overlay(_runtimeText['en-US']!, _runtimeText['ja-JP']!),
  'ko-KR': _overlay(_runtimeText['en-US']!, _runtimeText['ko-KR']!),
};

Map<String, String> _overlay(
  Map<String, String> base,
  Map<String, String> patch,
) =>
    {...base, ...patch};

String runtimeTextFor(Object? language, String key) {
  final code = normalizeAppLocaleCode(language);
  return _runtimeAliases[code]?[key] ??
      _runtimeText[code]?[key] ??
      _runtimeText['en-US']?[key] ??
      _runtimeText['zh-CN']?[key] ??
      key;
}

String runtimeFormatFor(
  Object? language,
  String key,
  Map<String, Object?> values,
) {
  var text = runtimeTextFor(language, key);
  for (final entry in values.entries) {
    text = text.replaceAll('{${entry.key}}', entry.value?.toString() ?? '');
  }
  return text;
}
